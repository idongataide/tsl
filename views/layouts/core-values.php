        <script>
            document.addEventListener('DOMContentLoaded', function () {
                const featureBoxes = document.querySelectorAll('.feature-box');
                const secondBox = document.querySelector('.feature-box.active');

                featureBoxes.forEach((box) => {
                    box.addEventListener('mouseenter', function () {
                        // If the hovered box is not the active one, remove the active class from the second box
                        if (!this.classList.contains('active')) {
                            secondBox.classList.remove('active');
                        }
                    });

                    box.addEventListener('mouseleave', function () {
                        // Re-add the active class to the second box only if the mouse is NOT hovering over the second box
                        if (!secondBox.matches(':hover')) {
                            secondBox.classList.add('active');
                        }
                    });
                });

                // Ensure the second box retains the active class if hovered
                secondBox.addEventListener('mouseenter', function () {
                    this.classList.add('active');
                });

                // When leaving the second box, remove the active class if mouse is not on any other box
                secondBox.addEventListener('mouseleave', function () {
                    if (!Array.from(featureBoxes).some(box => box.matches(':hover'))) {
                        this.classList.remove('active');
                    }
                });
            });
        </script>



